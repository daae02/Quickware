    public void addDealers() throws ProtocolException, IOException, IOException, MalformedURLException, MalformedURLException, JSONException{
        javax.swing.JPanel cont = new javax.swing.JPanel();
        cont.setLayout(new BoxLayout(cont, BoxLayout.Y_AXIS));
        Requester reqs = new Requester();
        try {
            repartidores = reqs.requestRepartidores();
        } catch (JSONException ex) {
            Logger.getLogger(PanelRepartidores.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Repartidor repartidor : repartidores) {
            PanelRepartidor p = new PanelRepartidor(repartidor);
            cont.add(p);
        }
        scrollDataPane.add(cont);
    }
